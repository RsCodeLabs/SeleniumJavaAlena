package com.javapractice.conditionalStatements;

public class EvenOrOdd {
    public static void main(String[] args) {
        int number = 11;
        if(number%2==0){
            System.out.println("The number " + number + " is even.");
        } else {
            System.out.println("The number " + number + " is odd.");
        }
    }
}
