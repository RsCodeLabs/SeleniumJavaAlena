package com.javapractice.dataTypes;

public class liertalsEx {
    public static void main(String[] args) {


        // Integer Literals
        int num = 100; // Decimal
        System.out.println("Integer Literal: " + num);
        int hexNum = 0x64; // Hexadecimal
        System.out.println("Hexadecimal Literal: " + hexNum);
        int octalNum = 0144; // Octal
        System.out.println("Octal Literal: " + octalNum);
        int binNum = 0b1100100; // Binary
        System.out.println("Binary Literal: " + binNum);

        char ch = 'A';
        System.out.println("Character Literal: " + ch);


        char ch1 = '\u0052'; // Unicode representation of 'A'
        System.out.println("Unicode Character Literal: " + ch1);



    }


}
